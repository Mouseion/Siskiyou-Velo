<h1>2009 - Local Racing Schedule and Results</h1>
<p>Southern Oregon's racing schedule varies from year-to-year, but the 2009 schedule begins as usual with the <a href="http://www.obra.org/flyers/2009/so_oregon_tt.html">Southern Oregon Time Trial Series</a> in March. The <a href="http://www.mudslingerevents.com/blog/table-rock-road-race/">Table Rock Road Race</a> really gets the ball rolling in April before the Thursday night series keeps the speed high throughout the summer.  The mountain bike season really gets rolling with the <a href="http://www.somba.org/springthaw/">Spring Thaw</a> a few weeks later. The traditional closer to the season for both road and mountain bike racers is the Jean Chinn - Mt Ashland Memorial Hillclimb. Finally, the Southern Oregon Outlaw Cyclocross series is held in over 5 weeks of October and November with a clinic held the week before the first race.</p>

<h1>Southern Oregon Time Trial Series</h1>
<p>The Southern Oregon Time Trial Series is a set of three separate individual time trials each on a different course with the cumulative time deciding the overall winner. The first race of 2009 will also feature a Team Time Trial.</p>

	
