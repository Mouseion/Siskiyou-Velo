<h2>Personal Message</h2>
<p>
Ally Grew up in the backwoods of very Northern California, or commonly known as the Great State of Jefferson. Ally started bike racing in her senior year of high school for a senior project and to cross train for wrestling. After high school she attended Lassen College to wrestle and put cycling on the back burner. After a season of wrestling was over Ally began her passion for cycling. Accepting a scholarship for cycling at Lees-McRae College her career began to blossom. Allys hard worth ethic and discipline helped her compete in MTB, CX, and Road all while working toward a History degree. She learned a great deal working with the collegiate team and being part of multiple collegiate team National Championships. In a matter of years she was racing in Europe and competing at the world class level for women's cycling.
</p>
